package com.address;

public class EtcDAO {
	
	private String adm_cd;
	private int population;
	private int family;
	private String tupyogu_addr;
	private int elector_cnt;
	private String tupyogu_name;
	public String getAdm_cd() {
		return adm_cd;
	}
	public void setAdm_cd(String adm_cd) {
		this.adm_cd = adm_cd;
	}
	public int getPopulation() {
		return population;
	}
	public void setPopulation(int population) {
		this.population = population;
	}
	public int getFamily() {
		return family;
	}
	public void setFamily(int family) {
		this.family = family;
	}
	public String getTupyogu_addr() {
		return tupyogu_addr;
	}
	public void setTupyogu_addr(String tupyogu_addr) {
		this.tupyogu_addr = tupyogu_addr;
	}
	public int getElector_cnt() {
		return elector_cnt;
	}
	public void setElector_cnt(int elector_cnt) {
		this.elector_cnt = elector_cnt;
	}
	public String getTupyogu_name() {
		return tupyogu_name;
	}
	public void setTupyogu_name(String tupyogu_name) {
		this.tupyogu_name = tupyogu_name;
	}
}
